    private void tabelBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBukuMouseClicked
        // TODO add your handling code here:
        int baris = tabelBuku.rowAtPoint(evt.getPoint());
        String id = tabelBuku.getValueAt(baris, 1).toString();
        txtID.setText(id);

        String nama = tabelBuku.getValueAt(baris, 2).toString();
        txtNama.setText(nama);

        String pil = tabelBuku.getValueAt(baris, 3).toString();
        cbPilih.setSelectedItem(pil);

        String alamat = tabelBuku.getValueAt(baris, 4).toString();
        txtPenerbit.setText(alamat);

        String email = tabelBuku.getValueAt(baris, 5).toString();
        txtEmail.setText(email);

        String telp = tabelBuku.getValueAt(baris, 6).toString();
        txtAlamat.setText(telp);

    }//GEN-LAST:event_tabelBukuMouseClicked

