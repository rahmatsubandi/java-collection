    //isi dari variabel tampilkan_data
    private void tampilkan_data(){
        
        //nama tabel ketika sudah disave
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID");
        model.addColumn("Judul Buku");
        model.addColumn("Tahun Liris");
        model.addColumn("Nama Penerbit");
        model.addColumn("Email Penerbit");
        model.addColumn("Alamat Penerbit");
        
        try{
            int no = 1; //dari id 1
            String sql = "SELECT * FROM buku"; //mengambil data di tabel buku
            java.sql.Connection conn = (Connection)Koneksi.configDB(); //dengan koneksi
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)}); 
            }
            tabelBuku.setModel(model);            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }

