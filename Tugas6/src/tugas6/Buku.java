    private void tbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbSaveActionPerformed
        // TODO add your handling code here:
        try{
            //memasukan data yang diketikan diform kedatabase
            String sql = "INSERT INTO buku VALUES ('"+txtID.getText()+"','"+txtNama.getText()+"','"+cbPilih.getSelectedItem()+"','"+txtPenerbit.getText()+"','"+txtEmail.getText()+"','"+txtAlamat.getText()+"')";
            java.sql.Connection conn = (Connection)Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil..");
            tampilkan_data();
            kosongkan_form();
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tbSaveActionPerformed
    
